#include "main.h"

/**
 * puts_half - prints half of the string
 * @str: input
 * @s: a pointer to an int that will be changed
 * return: half the input
 */

void puts_half(char *str)
{
	int u;

	u = 0;
	while (s[u] != '\0')
	{
		u++;
	}

	str = (s[u] / 2);
	{
		_putchar(*str);
		str++;
	}

	_putchar('\n');
}
